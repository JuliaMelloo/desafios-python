frase = 'Curso em Vídeo Python'
print(frase[3])  # Quarta letra da frase
print(frase[3:13])  # Quarta letra até a doze
print(frase[:13])  # Inicio até a letra doze
print(frase[13:])  # Letra doze até o final
print(frase[1:15:2])  # Letra dois até a catorze pulando de 2 em 2.
print(frase[::2])  # Do começo ao fim pulando de 2 em 2
print(frase.count('o'))  # Quantas vezes aparasse o caractere 'o' dentro da frase
print(frase.upper().count('O'))  # Colocou a frase para letras maiúsculas e contou quantas letras 'O' aparecem
print(len(frase))  # Quantos indices a frase tem
print(frase.replace('Python', 'Android'))  # Trocar caracteres
print('Curso' in frase)  # A palavra "curso" está dentro da frase?
print(frase.find('Curso'))  # Em qual posição 'Curso' se encontra





